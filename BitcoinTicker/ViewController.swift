import UIKit
import SwiftyJSON
import Alamofire

class ViewController: UIViewController, UIPickerViewDataSource, UIPickerViewDelegate {
    
    let baseURL = "https://apiv2.bitcoinaverage.com/indices/global/ticker/BTC"
    let currencyArray = ["AUD", "BRL","CAD","CNY","EUR","GBP","HKD","IDR","ILS","INR","JPY","MXN","NOK","NZD","PLN","RON","RUB","SEK","SGD","USD","ZAR"]
    let currencySymbolsArray = ["$", "R$", "$", "¥", "€", "£", "$", "Rp", "₪", "₹", "¥", "$", "kr", "$", "zł", "lei", "₽", "kr", "$", "$", "R"]
    var finalURL = ""
    
    var currencyIndex = 0

    //Pre-setup IBOutlets
    @IBOutlet weak var bitcoinPriceLabel: UILabel!
    @IBOutlet weak var currencyPicker: UIPickerView!
    

    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        currencyPicker.delegate = self
        currencyPicker.dataSource = self
       
    }

    
    //TODO: Place your 3 UIPickerView delegate methods here
    
    
    

    
    
    
    
    //MARK: - Networking
    /***************************************************************/
    
    func getBitcoinCurrencyData(url: String) {
        
        Alamofire.request(url, method: .get)
            .responseJSON { response in
                if response.result.isSuccess {

                    print("Sucess! Got the Bitcoin currency data")
                    let bitcoinDataJSON : JSON = JSON(response.result.value!)
                    print(bitcoinDataJSON)

                    self.updateBitcoinDataToUI(json: bitcoinDataJSON)

                } else {
                    print("Error: \(String(describing: response.result.error))")
                    self.bitcoinPriceLabel.text = "Connection Issues"
                }
            }

    }


    
    
    //MARK: - JSON Parsing
    /***************************************************************/
    
    func updateBitcoinDataToUI(json : JSON) {
        
        if let bitcoinDataJSON = json["bid"].double {
            print("test success")
            
            let dailyData = json["bid"].stringValue
            bitcoinPriceLabel.text =  "\(currencySymbolsArray[currencyIndex]) \(dailyData)"

        }
        else {
            
            bitcoinPriceLabel.text = "Data is unavailable"

        }
    }
    

    func numberOfComponents(in pickerView: UIPickerView) -> Int {
        return 1
    }
    
    func pickerView(_ pickerView: UIPickerView, numberOfRowsInComponent component: Int) -> Int {
        return currencyArray.count
    }
    
    func pickerView(_ pickerView: UIPickerView, titleForRow row: Int, forComponent component: Int) -> String? {
        return currencyArray[row]
    }
    
    func pickerView(_ pickerView: UIPickerView, didSelectRow row: Int, inComponent component: Int) {
        //print(row)
        print(currencyArray[row])
        
        currencyIndex = row
        print ("Currecy index is now... : \(currencyIndex)")
        
        finalURL = baseURL + currencyArray[row]
        print(finalURL)
        
        getBitcoinCurrencyData(url: finalURL)
    }
    
    

}

